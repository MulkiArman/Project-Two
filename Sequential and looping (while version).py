# Sequential and looping (while version)
print('Arman Receives orders from mom, to make 10  coffee')
print('Arman obeyed mom orders, then rushed to make 10  coffee')

Amount_of_coffee = 10
Coffee_that_has_been_made = 0
print('Coffee in the making process')

while Coffee_that_has_been_made < Amount_of_coffee:
    Coffee_that_has_been_made = Coffee_that_has_been_made + 1
    print(f'Coffe {Coffee_that_has_been_made} done')

print('All coffe is made mom')
